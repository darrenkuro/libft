#include <stddef.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;

	i = 0;
	if (!dst && !src)
		return (NULL);
	while (++i <= len)
		*(char *)dst++ = *(char *)src++;
	return (dst - i + 1);
}
