char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i])
		;
	if (c == '\0')
		return ((char *) &s[i]);
	while (--i >= 0)
		if (s[i] == c)
			return ((char *) &s[i]);
	return ((void *) 0);
}
